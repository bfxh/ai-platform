i18n_path = os.path.join(BASE, "packages", "core", "i18n", "index.tsx")
with open(i18n_path, "r", encoding="utf-8") as f:
    content = f.read()

dict_start = content.find('zh: {')
dict_end = content.find('\n  },\n};', dict_start)

if dict_start > 0 and dict_end > 0:
    existing_dict_text = content[dict_start+5:dict_end]
    existing_keys = set(re.findall(r'"([^"]+)":\s*"', existing_dict_text))
    
    new_entries = []
    for key, val in sorted(new_translations.items()):
        if key not in existing_keys:
            escaped_val = val.replace("\\", "\\\\").replace('"', '\\"')
            new_entries.append(f'    "{key}": "{escaped_val}",')
    
    if new_entries:
        insert_pos = dict_end
        new_text = "\n" + "\n".join(new_entries) + "\n"
        content = content[:insert_pos] + new_text + content[insert_pos:]
        with open(i18n_path, "w", encoding="utf-8") as f:
            f.write(content)
        print(f"Added {len(new_entries)} new translations to zh dictionary")
    else:
        print("No new translations to add")
else:
    print("ERROR: Could not find zh dictionary in index.tsx")

print("Done updating dictionary!")
