def main():
    """主函数"""
    if len(sys.argv) < 2:
        print(__doc__)
        return
    
    cmd = sys.argv[1]
    godot = GodotMCP()
    
    if cmd == "project":
        if len(sys.argv) < 3:
            print("用法: godot_mcp.py project <create|open|analyze>")
            return
        
        subcmd = sys.argv[2]
        
        if subcmd == "create":
            if len(sys.argv) < 4:
                print("用法: godot_mcp.py project create <name> [template]")
                return
            
            name = sys.argv[3]
            template = sys.argv[4] if len(sys.argv) > 4 else "2d"
            
            result = godot.project_create(name, template)
            
            if result.get("success"):
                print("项目已创建: " + result['project_path'])
                print("  名称: " + result['name'])
                print("  模板: " + result['template'])
            else:
                print("创建失败: " + result.get('error'))
        
        elif subcmd == "open":
            if len(sys.argv) < 4:
                print("用法: godot_mcp.py project open <path>")
                return
            
            result = godot.project_open(sys.argv[3])
            print(result.get("message") or result.get("error"))
        
        elif subcmd == "analyze":
            if len(sys.argv) < 4:
                print("用法: godot_mcp.py project analyze <path>")
                return
            
            result = godot.project_analyze(sys.argv[3])
            
            if result.get("success"):
                print("项目分析: " + result['project_name'])
                print("=" * 60)
                print("场景: " + str(result['stats']['scenes']))
                print("GDScript: " + str(result['stats']['scripts_gd']))
                print("C#: " + str(result['stats']['scripts_cs']))
                print("资源: " + str(result['stats']['resources']))
                print("纹理: " + str(result['stats']['textures']))
                print("音频: " + str(result['stats']['audio']))
                print("总计: " + str(result['total_files']) + " 个文件")
    
    elif cmd == "scene":
        if len(sys.argv) < 3:
            print("用法: godot_mcp.py scene <create|add_node>")
            return
        
        scene_cmd = sys.argv[2]
        
        if scene_cmd == "create":
            if len(sys.argv) < 5:
                print("用法: godot_mcp.py scene create <project_path> <name> [node_type]")
                return
            
            project_path = sys.argv[3]
            name = sys.argv[4]
            node_type = sys.argv[5] if len(sys.argv) > 5 else "Node2D"
            
            result = godot.scene_create(project_path, name, node_type)
            
            if result.get("success"):
                print("场景已创建: " + result['scene_path'])
            else:
                print("创建失败: " + result.get('error'))
    
    elif cmd == "script":
        if len(sys.argv) < 4:
            print("用法: godot_mcp.py script generate <prompt> [node_type] [language]")
            return
        
        prompt = sys.argv[2]
        node_type = sys.argv[3] if len(sys.argv) > 3 else "Node2D"
        language = sys.argv[4] if len(sys.argv) > 4 else "gdscript"
        
        result = godot.script_generate(prompt, node_type, language)
        
        if result.get("success"):
            print("生成的 " + result['language'] + " 代码:")
            print("=" * 60)
            print(result['code'])
    
    elif cmd == "import_assets":
        if len(sys.argv) < 4:
            print("用法: godot_mcp.py import_assets <project_path> <file1> [file2] ...")
            return
        
        project_path = sys.argv[2]
        files = sys.argv[3:]
        
        result = godot.import_assets(project_path, files)
        
        if result.get("success"):
            print("导入成功: " + str(result['count']) + " 个文件")
            for f in result['imported']:
                print("  - " + f)
        else:
            print("导入失败: " + result.get('error'))
    
    elif cmd == "export":
        if len(sys.argv) < 4:
            print("用法: godot_mcp.py export <project_path> <platform> [output_path]")
            return
        
        project_path = sys.argv[2]
        platform = sys.argv[3]
        output_path = sys.argv[4] if len(sys.argv) > 4 else None
        
        result = godot.export_project(project_path, platform, output_path)
        
        if result.get("success"):
            print("导出成功: " + result['output'])
            print("  平台: " + result['platform'])
