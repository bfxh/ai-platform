# ============================================================
# Godot MCP
# ============================================================
class GodotMCP:
    """Godot 引擎 MCP 工具"""
    
    def __init__(self):
        self.godot_path = self._find_godot()
    
    def _find_godot(self) -> Optional[Path]:
        """查找 Godot 可执行文件"""
        for path in GODOT_PATHS:
            if path.exists():
                return path
        return None
    
    def _generate_uid(self) -> str:
        """生成 Godot UID"""
        return str(uuid.uuid4()).replace("-", "")[:13]
    
    def _run_godot(self, args: List[str], cwd: Path = None) -> subprocess.CompletedProcess:
        """运行 Godot 命令"""
        if not self.godot_path:
            raise FileNotFoundError("未找到 Godot 可执行文件")
        
        cmd = [str(self.godot_path), "--headless"] + args
        return subprocess.run(cmd, capture_output=True, text=True, cwd=cwd)
    
    # ============================================================
    # 项目管理
    # ============================================================
    def project_create(self, name: str, template: str = "2d", path: str = None) -> Dict:
        """创建 Godot 项目"""
        try:
            # 确定项目路径
            if path:
                project_path = Path(path) / name
            else:
                project_path = PROJECTS_PATH / name
            
            project_path.mkdir(parents=True, exist_ok=True)
            
            # 获取模板配置
            template_config = PROJECT_TEMPLATES.get(template, PROJECT_TEMPLATES["2d"])
            
            # 创建 project.godot
            features_str = str(template_config['features']).replace("'", '"')
            rendering_method = template_config['features'][1].lower().replace(' ', '_')
            
            project_godot = """; Engine Configuration File
; Godot version: 4.x
; Generated by Godot MCP

[application]
config/name=""" + name + """
config/description=""" + template_config['description'] + """
config/features=PackedStringArray(""" + features_str + """)
config/icon="res://icon.svg"

[rendering]
renderer/rendering_method=""" + rendering_method + """
"""
            
            with open(project_path / "project.godot", "w", encoding="utf-8") as f:
                f.write(project_godot)
            
            # 创建目录结构
            (project_path / "scenes").mkdir(exist_ok=True)
            (project_path / "scripts").mkdir(exist_ok=True)
            (project_path / "assets").mkdir(exist_ok=True)
            (project_path / "assets" / "textures").mkdir(exist_ok=True)
            (project_path / "assets" / "audio").mkdir(exist_ok=True)
            (project_path / "assets" / "fonts").mkdir(exist_ok=True)
            
            # 创建默认场景
            self.scene_create(str(project_path), "MainScene", node_type=template_config["default_scene"])
            
            # 设置主场景
            self._set_main_scene(project_path, "scenes/MainScene.tscn")
            
            return {
                "success": True,
                "project_path": str(project_path),
                "name": name,
                "template": template,
                "description": template_config["description"]
            }
        
        except Exception as e:
            return {"success": False, "error": str(e)}
    
    def _set_main_scene(self, project_path: Path, scene_path: str):
        """设置主场景"""
        project_file = project_path / "project.godot"
        
        with open(project_file, "r", encoding="utf-8") as f:
            content = f.read()
        
        # 添加主场景配置
        if "run/main_scene" not in content:
            content += '\n[run]\nmain_scene="res://' + scene_path + '"\n'
            
            with open(project_file, "w", encoding="utf-8") as f:
                f.write(content)
    
    def project_open(self, path: str) -> Dict:
        """打开 Godot 项目"""
        try:
            project_path = Path(path)
            
            if not (project_path / "project.godot").exists():
                return {"success": False, "error": "不是有效的 Godot 项目"}
            
            # 启动 Godot 编辑器
            if self.godot_path:
                subprocess.Popen([
                    str(self.godot_path),
                    "--editor",
                    "--path", str(project_path)
                ])
                
                return {
                    "success": True,
                    "project_path": str(project_path),
                    "message": "Godot 编辑器已启动"
                }
            else:
                return {"success": False, "error": "未找到 Godot 编辑器"}
        
        except Exception as e:
            return {"success": False, "error": str(e)}
    
    def project_analyze(self, path: str) -> Dict:
        """分析 Godot 项目"""
        try:
            project_path = Path(path)
            
            if not (project_path / "project.godot").exists():
                return {"success": False, "error": "不是有效的 Godot 项目"}
            
            # 统计项目信息
            stats = {
                "scenes": len(list(project_path.rglob("*.tscn"))),
                "scripts_gd": len(list(project_path.rglob("*.gd"))),
                "scripts_cs": len(list(project_path.rglob("*.cs"))),
                "resources": len(list(project_path.rglob("*.tres"))),
                "textures": len(list(project_path.rglob("*.png"))) + len(list(project_path.rglob("*.jpg"))),
                "audio": len(list(project_path.rglob("*.ogg"))) + len(list(project_path.rglob("*.wav"))),
            }
            
            # 读取项目配置
            with open(project_path / "project.godot", "r", encoding="utf-8") as f:
                project_config = f.read()
            
            # 提取项目名称
            name_match = re.search(r'config/name="([^"]+)"', project_config)
            project_name = name_match.group(1) if name_match else "Unknown"
            
            return {
                "success": True,
                "project_name": project_name,
                "project_path": str(project_path),
                "stats": stats,
                "total_files": sum(stats.values())
            }
        
        except Exception as e:
            return {"success": False, "error": str(e)}
    
    # ============================================================
    # 场景管理
    # ============================================================
    def scene_create(self, project_path: str, name: str, node_type: str = "Node2D") -> Dict:
        """创建场景"""
        try:
            project = Path(project_path)
            if not project.exists():
                return {"success": False, "error": "项目路径不存在: " + str(project_path)}
            
            scenes_dir = project / "scenes"
            scenes_dir.mkdir(exist_ok=True)
            scene_path = scenes_dir / (name + ".tscn")
            
            # 生成场景内容
            scene_content = """[gd_scene load_steps=1 format=3 uid="uid://""" + self._generate_uid() + """"]

[node name=""" + name + """ type=""" + node_type + """]
"""
            
            with open(scene_path, "w", encoding="utf-8") as f:
                f.write(scene_content)
            
            return {
                "success": True,
                "scene_path": str(scene_path),
                "name": name,
                "node_type": node_type
            }
        
        except Exception as e:
            return {"success": False, "error": str(e)}
    
    def scene_add_node(self, scene_path: str, node_name: str, node_type: str, parent: str = ".") -> Dict:
        """向场景添加节点"""
        try:
            scene_file = Path(scene_path)
            
            with open(scene_file, "r", encoding="utf-8") as f:
                content = f.read()
            
            # 添加节点
            node_entry = '\n[node name="' + node_name + '" type="' + node_type + '" parent="' + parent + '"]\n'
            
            content += node_entry
            
            with open(scene_file, "w", encoding="utf-8") as f:
                f.write(content)
            
            return {
                "success": True,
                "scene": str(scene_path),
                "node": node_name,
                "type": node_type
            }
        
        except Exception as e:
            return {"success": False, "error": str(e)}
    
    # ============================================================
    # 脚本生成
    # ============================================================
    def script_generate(self, prompt: str, node_type: str = "Node2D", language: str = "gdscript") -> Dict:
        """生成脚本"""
        try:
            if language == "gdscript":
                return self._generate_gdscript(prompt, node_type)
            elif language == "csharp":
                return self._generate_csharp(prompt, node_type)
            else:
                return {"success": False, "error": "不支持的语言: " + language}
        
        except Exception as e:
            return {"success": False, "error": str(e)}
    
