    # ============================================================
    # 资源导入
    # ============================================================
    def import_assets(self, project_path: str, files: List[str], target_dir: str = "assets") -> Dict:
        """导入资源"""
        try:
            project = Path(project_path)
            imported = []
            failed = []
            
            for file_path in files:
                src = Path(file_path)
                if not src.exists():
                    failed.append({"file": file_path, "reason": "文件不存在"})
                    continue
                
                # 确定目标目录
                if src.suffix in ['.png', '.jpg', '.jpeg', '.svg', '.webp']:
                    dst_dir = project / target_dir / "textures"
                elif src.suffix in ['.ogg', '.wav', '.mp3']:
                    dst_dir = project / target_dir / "audio"
                elif src.suffix in ['.ttf', '.otf']:
                    dst_dir = project / target_dir / "fonts"
                elif src.suffix in ['.gltf', '.glb', '.obj', '.fbx']:
                    dst_dir = project / target_dir / "models"
                else:
                    dst_dir = project / target_dir
                
                dst_dir.mkdir(parents=True, exist_ok=True)
                
                # 复制文件
                import shutil
                dst = dst_dir / src.name
                shutil.copy2(src, dst)
                
                imported.append(str(dst))
            
            return {
                "success": True,
                "imported": imported,
                "failed": failed,
                "count": len(imported)
            }
        
        except Exception as e:
            return {"success": False, "error": str(e)}
    
    # ============================================================
    # 导出构建
    # ============================================================
    def export_project(self, project_path: str, platform: str, output_path: str = None) -> Dict:
        """导出项目"""
        try:
            project = Path(project_path)
            
            if not (project / "project.godot").exists():
                return {"success": False, "error": "不是有效的 Godot 项目"}
            
            preset = EXPORT_PRESETS.get(platform)
            if not preset:
                return {"success": False, "error": "不支持的平台: " + platform}
            
            # 确定输出路径
            if not output_path:
                output_path = str(project / "builds" / platform / (project.name + preset['extension']))
            
            output = Path(output_path)
            output.parent.mkdir(parents=True, exist_ok=True)
            
            # 导出
            result = self._run_godot([
                "--export-release", preset["name"], str(output)
            ], cwd=project)
            
            if result.returncode == 0:
                return {
                    "success": True,
                    "platform": platform,
                    "output": str(output),
                    "size": output.stat().st_size if output.exists() else 0
                }
            else:
                return {
                    "success": False,
                    "error": result.stderr or "导出失败",
                    "stdout": result.stdout
                }
        
        except Exception as e:
            return {"success": False, "error": str(e)}

