class MCPInterface:
    """MCP 接口"""
    
    def __init__(self):
        self.godot = GodotMCP()
    
    def handle(self, request: Dict) -> Dict:
        """处理 MCP 请求"""
        action = request.get("action")
        params = request.get("params", {})
        
        if action == "project_create":
            return self.godot.project_create(
                params.get("name"),
                params.get("template", "2d"),
                params.get("path")
            )
        
        elif action == "project_open":
            return self.godot.project_open(params.get("path"))
        
        elif action == "project_analyze":
            return self.godot.project_analyze(params.get("path"))
        
        elif action == "scene_create":
            return self.godot.scene_create(
                params.get("project_path"),
                params.get("name"),
                params.get("node_type", "Node2D")
            )
        
        elif action == "scene_add_node":
            return self.godot.scene_add_node(
                params.get("scene_path"),
                params.get("node_name"),
                params.get("node_type"),
                params.get("parent", ".")
            )
        
        elif action == "script_generate":
            return self.godot.script_generate(
                params.get("prompt"),
                params.get("node_type", "Node2D"),
                params.get("language", "gdscript")
            )
        
        elif action == "import_assets":
            return self.godot.import_assets(
                params.get("project_path"),
                params.get("files", []),
                params.get("target_dir", "assets")
            )
        
        elif action == "export_project":
            return self.godot.export_project(
                params.get("project_path"),
                params.get("platform"),
                params.get("output_path")
            )
        
        else:
            return {"success": False, "error": "未知操作: " + action}

