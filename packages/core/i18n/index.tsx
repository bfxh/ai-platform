let currentLocale: Locale = "zh";
let currentDict: Record<string, string> = dictionaries.zh;

export function registerDictionary(locale: Locale, dict: Record<string, string>) {
  dictionaries[locale] = { ...dictionaries[locale], ...dict };
  if (locale === currentLocale) currentDict = dictionaries[locale];
}

export function t(key: string, params?: Record<string, string | number>): string {
  let text = currentDict[key] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(new RegExp(`\\{\\{${k}\\}\\}`, "g"), String(v));
    }
  }
  return text;
}

export function tn(key: string, keyPlural: string, count: number, params?: Record<string, string | number>): string {
  const k = count === 1 ? key : keyPlural;
  return t(k, { ...params, count: String(count) });
}

interface I18nContextValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: typeof t;
}

const I18nContext = createContext<I18nContextValue>({
  locale: "zh",
  setLocale: () => {},
  t,
});

export function I18nProvider({ children, defaultLocale = "zh" }: { children: ReactNode; defaultLocale?: Locale }) {
  const [locale, setLocaleState] = useState<Locale>(defaultLocale);

  const setLocale = useCallback((newLocale: Locale) => {
    currentLocale = newLocale;
    currentDict = dictionaries[newLocale];
    setLocaleState(newLocale);
  }, []);

  return (
    <I18nContext.Provider value={{ locale, setLocale, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}

